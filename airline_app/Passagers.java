    private void DisplayPassagers() {
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinedb", "root", "");
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from PassagersTbl");
            PassagersTable.setModel(DbUtils.resultSetToTableModel(Rs));
        } catch (Exception e) {

        }

    }
    int PassId = 0;

    private void CountPassagers() {
        try {
            St1 = Con.createStatement();
            Rs1 = St1.executeQuery("Select Max(PId) from PassagersTbl");
            Rs1.next();
            PassId = Rs1.getInt(1) + 1;
        } catch (Exception e) {
        }
    }

